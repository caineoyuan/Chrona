// aliases for legacy names used across the app
ICONS['fire-element'] = ICONS.fire
ICONS['plus-math'] = ICONS.plus

export default function Icon({ name, size = 20, className = '', alt = '' }) {
  const entry = ICONS[name] || ICONS.timer
  if (entry && entry.img) {
    return (
      <img
        className={`gi ${className}`.trim()}
        src={entry.img}
        width={size}
        height={size}
        alt={alt}
        aria-hidden={alt ? undefined : true}
        draggable={false}
      />
    )
  }
  const isObj = entry && entry.el
  const content = isObj ? entry.el : entry
  const viewBox = isObj && entry.vb ? entry.vb : '0 0 24 24'
  return (
    <svg
      className={`gi ${className}`.trim()}
      width={size}
      height={size}
      viewBox={viewBox}
      fill="none"
      strokeLinecap="round"
      strokeLinejoin="round"
      role={alt ? 'img' : 'presentation'}
      aria-label={alt || undefined}
      aria-hidden={alt ? undefined : true}
    >
      {content}
    </svg>
  )
}
